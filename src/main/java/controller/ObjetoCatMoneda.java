
package controller;

/*
    *   CREANDO OBJETO PARA LA MONEDA CONSUMIDA DEL WEB SERVICES
*/
public class ObjetoCatMoneda {

    int modena;
    String descripcion;


    public ObjetoCatMoneda() {
    }

    public int getModena() {
        return modena;
    }

    public void setModena(int modena) {
        this.modena = modena;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

}
